"""External benchmark — our samplics wrapper vs. R's ``survey::calibrate``.

Two fixtures, generated by ``scripts/generate_apistrat.R``:

    1. apistrat_linear.csv — canonical apistrat dataset, LOOSE bounds
       ``[0.1, 10]`` so truncation barely bites. This isolates linear-
       calibration math parity.

    2. balanced_tight.csv — synthetic 500-row sample where bounds
       ``[0.5, 2]`` never bind on either side. Tests bit-close parity
       under our real PoliticoResto setup.

Reference category alignment: our Python wrapper drops the LAST
category per dimension; R drops the FIRST (alphabetically) by default.
The Python marginal dicts below are ordered so the category R drops is
last in the dict — same constraints on both sides.

If this test fails, it means our wrapper's math has diverged from R's
gold-standard linear calibration. The test is NON-optional: mathematical
correctness is what this whole worker sells.
"""

from __future__ import annotations

from pathlib import Path

import numpy as np
import pandas as pd
import pytest

from weighting.calibration import calibrate

DATA_DIR = Path(__file__).parent / "data"


@pytest.mark.external_benchmark
@pytest.mark.skipif(
    not (DATA_DIR / "apistrat_linear.csv").exists(),
    reason="Run scripts/generate_apistrat.R to produce benchmark fixtures",
)
def test_apistrat_linear_matches_r_survey() -> None:
    df = pd.read_csv(DATA_DIR / "apistrat_linear.csv")
    pop_total = 6194

    # Dict order: R-reference category LAST (= E for stype, No for awards).
    marginals = {
        "stype":  {"H": 755 / pop_total, "M": 1018 / pop_total, "E": 4421 / pop_total},
        "awards": {"Yes": 3194 / pop_total, "No": 3000 / pop_total},
    }
    r = calibrate(df, marginals, bounds=(0.1, 10.0))

    r_weights = df["r_weight"].to_numpy()
    ours = r.weights

    # Bounds honoured on both sides.
    assert ours.min() >= 0.1 - 1e-9
    assert ours.max() <= 10.0 + 1e-9

    # Element-wise comparison. With NO clipping, parity should be near-
    # floating-point-exact (see the balanced_tight fixture, which hits
    # 1e-6 parity). On apistrat_linear a handful of weights hit the
    # lower bound (0.1) on at least one side, and R's truncation is
    # an iterative CALMAR while ours is a single clip-and-slack pass
    # (see calibration.py docstring). The two disagree by up to ~0.12%
    # on the clipped subset. 5e-3 gives safe headroom while still
    # catching any real divergence.
    max_rel_diff = np.max(np.abs(ours - r_weights) / np.maximum(r_weights, 1e-9))
    assert max_rel_diff < 5e-3, (
        f"apistrat_linear: max relative weight diff vs R = {max_rel_diff:.3e} "
        f"(ours range [{ours.min():.3f}, {ours.max():.3f}], "
        f"R range [{r_weights.min():.3f}, {r_weights.max():.3f}])"
    )


@pytest.mark.external_benchmark
@pytest.mark.skipif(
    not (DATA_DIR / "balanced_tight.csv").exists(),
    reason="Run scripts/generate_apistrat.R to produce benchmark fixtures",
)
def test_balanced_tight_matches_r_survey() -> None:
    df = pd.read_csv(DATA_DIR / "balanced_tight.csv")

    # R drops sex=F (first alphabetically), age=old (first alphabetically).
    # Python wrapper drops LAST → put F and old last.
    marginals = {
        "sex": {"M": 0.48, "F": 0.52},
        "age": {"young": 0.40, "old": 0.60},
    }
    r = calibrate(df, marginals, bounds=(0.5, 2.0))

    r_weights = df["r_weight"].to_numpy()
    ours = r.weights

    assert ours.min() >= 0.5 - 1e-9
    assert ours.max() <= 2.0 + 1e-9
    assert r.n_clipped == 0  # bounds never bite on this fixture

    # Tight parity expected — no truncation on either side.
    max_rel_diff = np.max(np.abs(ours - r_weights) / np.maximum(r_weights, 1e-9))
    assert max_rel_diff < 1e-6, (
        f"balanced_tight: max relative weight diff vs R = {max_rel_diff:.3e}"
    )


@pytest.mark.external_benchmark
@pytest.mark.skipif(
    not (DATA_DIR / "deville_sarndal_toy.csv").exists(),
    reason="Run scripts/generate_deville_sarndal.R to produce this fixture",
)
def test_deville_sarndal_toy_matches_r_survey() -> None:
    """Small hand-designed n=20 case faithful to the 1992 paper's §5
    structure (two binary auxiliaries, skewed sample). R acts as the
    oracle for the expected output."""
    df = pd.read_csv(DATA_DIR / "deville_sarndal_toy.csv")
    # R drops the first level alphabetically: urban="no", edu="high".
    marginals = {
        "urban": {"yes": 0.60, "no": 0.40},
        "edu":   {"low": 0.55, "high": 0.45},
    }
    r = calibrate(df, marginals, bounds=(0.1, 10.0))
    r_weights = df["r_weight"].to_numpy()
    max_rel_diff = np.max(np.abs(r.weights - r_weights) / np.maximum(r_weights, 1e-9))
    assert max_rel_diff < 1e-6, (
        f"deville_sarndal_toy: max relative weight diff vs R = {max_rel_diff:.3e}"
    )
