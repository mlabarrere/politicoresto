#!/usr/bin/env Rscript
# Military-grade fixture bank — ~50 scenarios of R survey::calibrate
# outputs covering the decision space our wrapper ships against:
#
#   * n  ∈ {20, 100, 500, 2000}
#   * k_dims ∈ {1, 2, 3}                    (number of marginal dims)
#   * skew level ∈ {none, mild, severe}
#   * bounds tightness ∈ {loose=[0.1,10], tight=[0.5,2]}
#   * edge flags:
#       - baseline                          (none of the below)
#       - singleton_cat                     (one category has exactly 1 unit)
#       - unknown_bucket                    (some rows fall outside declared cats)
#       - dim_near_collinear                (two dims correlated in the sample)
#
# Not the full Cartesian product — a curated cross. Each scenario emits
# one CSV into data/grid/<id>.csv and one row into data/grid/index.yaml
# with the Python marginals the test should use.
#
# R drops the first factor level alphabetically. To keep Python and R
# solving the SAME constrained system, we order factor levels so the
# reference (first-alpha on R side) lands LAST in the Python dict. The
# index.yaml writes the Python dict order explicitly.
#
# Rerun with:
#   Rscript worker/tests/external_benchmark/scripts/generate_grid_bank.R

suppressPackageStartupMessages(library(survey))
set.seed(1729)  # Ramanujan, nothing stochastic should depend on this.

args <- commandArgs(trailingOnly = FALSE)
script_arg  <- args[grep("^--file=", args)]
script_path <- if (length(script_arg) > 0) sub("^--file=", "", script_arg[1]) else "."
script_dir  <- normalizePath(dirname(script_path), mustWork = FALSE)
out_dir <- file.path(script_dir, "..", "data", "grid")
dir.create(out_dir, showWarnings = FALSE, recursive = TRUE)

# Existing CSVs are refreshed every run — a stale file would mask drift.
for (f in list.files(out_dir, pattern = "\\.csv$", full.names = TRUE)) file.remove(f)

# yaml-lite: hand-emitted so we don't need the `yaml` R package.
index_lines <- c("# AUTO-GENERATED by scripts/generate_grid_bank.R.",
                 "# Do not hand-edit. Refresh via the R script.",
                 "scenarios:")

# Helper: build a sample frame + marginals for a scenario and call R calibrate.
run_scenario <- function(scenario_id, df, python_marginals, bounds,
                         r_formula, r_pop_totals, notes) {
  n <- nrow(df)
  ds <- svydesign(ids = ~1, weights = ~I(rep(1, n)), data = df)
  cal <- tryCatch(
    suppressWarnings(calibrate(ds, r_formula, population = r_pop_totals,
                               bounds = bounds, calfun = "linear")),
    error = function(e) {
      cat(sprintf("  SKIP  %s  (R failed: %s)\n", scenario_id, conditionMessage(e)))
      NULL
    }
  )
  if (is.null(cal)) return(invisible(NULL))
  w <- weights(cal)
  stopifnot(length(w) == n, all(is.finite(w)))
  stopifnot(all(w >= bounds[1] - 1e-9), all(w <= bounds[2] + 1e-9))
  out <- data.frame(df, r_weight = as.numeric(w), stringsAsFactors = FALSE)
  write.csv(out, file.path(out_dir, paste0(scenario_id, ".csv")), row.names = FALSE)

  # Emit index entry.
  index_lines <<- c(
    index_lines,
    sprintf("  - id: %s", scenario_id),
    sprintf("    notes: %s", shQuote(notes, type = "sh")),
    sprintf("    bounds: [%g, %g]", bounds[1], bounds[2]),
    "    marginals:"
  )
  for (dim_name in names(python_marginals)) {
    index_lines <<- c(index_lines, sprintf("      %s:", dim_name))
    for (cat in names(python_marginals[[dim_name]])) {
      index_lines <<- c(index_lines,
                        sprintf("        %s: %.10f",
                                cat, python_marginals[[dim_name]][[cat]]))
    }
  }
  cat(sprintf("  wrote %s  n=%d sum=%.3f range=[%.3f,%.3f]\n",
              scenario_id, n, sum(w), min(w), max(w)))
}

# Build a single-dim scenario. `freqs` gives the sample category counts,
# `pop_shares` the target shares. Factor levels ordered so alpha-first
# is the R reference — Python dict order reverses to put it last.
one_dim <- function(scenario_id, freqs, pop_shares, bounds, notes) {
  # Coerce freq/share inputs to simple named numeric vectors regardless
  # of whether the caller passed a list() or a named numeric.
  freqs_v <- unlist(freqs)
  shares_v <- unlist(pop_shares)

  all_cats <- names(freqs_v)
  alpha_first <- sort(all_cats)[1]
  non_ref <- setdiff(all_cats, alpha_first)
  factor_levels <- sort(all_cats)
  py_cats <- c(non_ref, alpha_first)  # Python dict order: reference last

  n <- sum(freqs_v)
  rows <- unlist(lapply(all_cats, function(c) rep(c, freqs_v[[c]])))
  df <- data.frame(
    dim1 = factor(rows, levels = factor_levels),
    stringsAsFactors = FALSE
  )
  r_pop_totals <- c(`(Intercept)` = n)
  for (c in non_ref) {
    r_pop_totals[[paste0("dim1", c)]] <- n * shares_v[[c]]
  }
  python_marginals <- list(dim1 = as.list(shares_v[py_cats]))
  run_scenario(scenario_id, df, python_marginals, bounds,
               ~dim1, r_pop_totals, notes)
}

two_dim <- function(scenario_id, df_cells, pop_shares, bounds, notes) {
  shares_d1 <- unlist(pop_shares$dim1)
  shares_d2 <- unlist(pop_shares$dim2)
  n <- length(df_cells$dim1)
  all_d1 <- sort(unique(df_cells$dim1))
  all_d2 <- sort(unique(df_cells$dim2))
  ref_d1 <- all_d1[1]; ref_d2 <- all_d2[1]
  non_ref_d1 <- setdiff(all_d1, ref_d1)
  non_ref_d2 <- setdiff(all_d2, ref_d2)

  df <- data.frame(
    dim1 = factor(df_cells$dim1, levels = all_d1),
    dim2 = factor(df_cells$dim2, levels = all_d2),
    stringsAsFactors = FALSE
  )
  r_pop_totals <- c(`(Intercept)` = n)
  for (c in non_ref_d1) r_pop_totals[[paste0("dim1", c)]] <- n * shares_d1[[c]]
  for (c in non_ref_d2) r_pop_totals[[paste0("dim2", c)]] <- n * shares_d2[[c]]

  python_marginals <- list(
    dim1 = as.list(shares_d1[c(non_ref_d1, ref_d1)]),
    dim2 = as.list(shares_d2[c(non_ref_d2, ref_d2)])
  )
  run_scenario(scenario_id, df, python_marginals, bounds,
               ~dim1 + dim2, r_pop_totals, notes)
}

three_dim <- function(scenario_id, cells, pop_shares, bounds, notes) {
  shares_d1 <- unlist(pop_shares$dim1)
  shares_d2 <- unlist(pop_shares$dim2)
  shares_d3 <- unlist(pop_shares$dim3)
  n <- length(cells$dim1)
  all_lists <- list(d1 = sort(unique(cells$dim1)),
                    d2 = sort(unique(cells$dim2)),
                    d3 = sort(unique(cells$dim3)))
  ref <- list(d1 = all_lists$d1[1], d2 = all_lists$d2[1], d3 = all_lists$d3[1])
  non_ref <- list(
    d1 = setdiff(all_lists$d1, ref$d1),
    d2 = setdiff(all_lists$d2, ref$d2),
    d3 = setdiff(all_lists$d3, ref$d3)
  )
  df <- data.frame(
    dim1 = factor(cells$dim1, levels = all_lists$d1),
    dim2 = factor(cells$dim2, levels = all_lists$d2),
    dim3 = factor(cells$dim3, levels = all_lists$d3),
    stringsAsFactors = FALSE
  )
  r_pop_totals <- c(`(Intercept)` = n)
  for (c in non_ref$d1) r_pop_totals[[paste0("dim1", c)]] <- n * shares_d1[[c]]
  for (c in non_ref$d2) r_pop_totals[[paste0("dim2", c)]] <- n * shares_d2[[c]]
  for (c in non_ref$d3) r_pop_totals[[paste0("dim3", c)]] <- n * shares_d3[[c]]
  python_marginals <- list(
    dim1 = as.list(shares_d1[c(non_ref$d1, ref$d1)]),
    dim2 = as.list(shares_d2[c(non_ref$d2, ref$d2)]),
    dim3 = as.list(shares_d3[c(non_ref$d3, ref$d3)])
  )
  run_scenario(scenario_id, df, python_marginals, bounds,
               ~dim1 + dim2 + dim3, r_pop_totals, notes)
}

