# Note: we considered an "unknown-bucket" edge scenario here
# (rows whose dim1 falls outside the declared marginals). By design,
# Python and R model this case differently — our wrapper gives
# out-of-scope rows a weight via the intercept constraint alone,
# while R's `calibrate` requires them to be filtered out of the
# design up-front. That's a legitimate semantic choice, not a parity
# bug, so we do NOT benchmark it here. Unknown-bucket handling is
# covered in worker/tests/unit/test_calibration.py::test_unknown_bucket_handled.

# E3 — near-collinear dims: dim2 ≈ dim1 so the 2D system is almost
# rank-deficient but not singular. Tests numerical stability.
{
  n <- 300
  set.seed(99)
  d1 <- sample(c("a", "b"), n, replace = TRUE, prob = c(0.6, 0.4))
  # dim2 follows dim1 95% of the time.
  d2 <- ifelse(runif(n) < 0.95,
               ifelse(d1 == "a", "y", "z"),
               ifelse(d1 == "a", "z", "y"))
  df_cells <- list(dim1 = d1, dim2 = d2)
  two_dim("edge_near_collinear_2d",
          df_cells = df_cells,
          pop_shares = list(dim1 = list(a = 0.50, b = 0.50),
                            dim2 = list(y = 0.50, z = 0.50)),
          bounds = c(0.1, 10),
          notes = "EDGE: dim1 and dim2 highly correlated; stresses numerical stability.")
}

