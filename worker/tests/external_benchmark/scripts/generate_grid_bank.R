make_indep_2d <- function(n, p_d1a, p_d2a, seed = 42) {
  set.seed(seed)
  list(
    dim1 = sample(c("a", "b"), n, replace = TRUE, prob = c(p_d1a, 1 - p_d1a)),
    dim2 = sample(c("y", "z"), n, replace = TRUE, prob = c(p_d2a, 1 - p_d2a))
  )
}

