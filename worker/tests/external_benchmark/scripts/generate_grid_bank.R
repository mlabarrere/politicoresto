# 2D with INSEE-flavoured 3-level sex × 2-level education
make_3x2 <- function(n_per_cell) {
  cells <- list(c("F", "high"), c("F", "low"),
                c("M", "high"), c("M", "low"),
                c("other", "high"), c("other", "low"))
  rows <- do.call(rbind, lapply(seq_along(cells), function(i) {
    cell <- cells[[i]]
    data.frame(dim1 = rep(cell[1], n_per_cell[i]),
               dim2 = rep(cell[2], n_per_cell[i]),
               stringsAsFactors = FALSE)
  }))
  list(dim1 = rows$dim1, dim2 = rows$dim2)
}
two_dim("2d_3x2_balanced",
        df_cells = make_3x2(c(30, 30, 25, 25, 5, 5)),
        pop_shares = list(
          dim1 = list(F = 0.52, M = 0.47, other = 0.01),
          dim2 = list(high = 0.45, low = 0.55)
        ),
        bounds = c(0.5, 2),
        notes = "2D 3-level × 2-level, small minority category; tight bounds.")

